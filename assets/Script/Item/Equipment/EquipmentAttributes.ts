export class EquipmentAttributes {
    /**
     * 生命值
     */
    readonly additionalHealth: number = 0;

    /**
     * 生命值倍率
     */
    readonly healthBoost: number = 0;

    /**
     * 额外生命
     */
    readonly extraHealth: number = 0;

    /**
     * 攻击力
     */
    readonly additionalDamage: number = 0;

    /**
     * 攻击力倍率
     */
    readonly damageBoost: number = 0;

    /**
     * 防御力
     */
    readonly additionalDefense: number = 0;

    /**
     * 防御力倍率
     */
    readonly defenseBoost: number = 0;

    /**
     * 暴击率
     */
    readonly criticalRate: number = 0;

    /**
     * 暴击伤害倍率
     */
    readonly criticalBoost: number = 0;
}
